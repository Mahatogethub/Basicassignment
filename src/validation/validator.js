//---------------------------- validation of string -----------------------------------------------------------

const isValidString = (Name) =>{
    return /^[a-zA-Z ]+$/.test(Name)
}

//--------------------------------------------------------------------------------------------------------------


//-------------------------------- validation of email -----------------------------------------------------
const isValidEmail = (Email) =>{
    return (/^\w+([\.-]?\w+)@\w+([\.-]?\w+)(\.\w{2,3})+$/).test(Email);
}

//-----------------------------------------------------------------------------------------------------------


//------------------------------------validation of age----------------------------------------------------------

const isValideAge = (Age) =>{
    return (/^\S[0-9]{0,3}$/).test(Age) 
}

//--------------------------------------------------------------------------------------------------------------



module.exports = {isValidString , isValidEmail , isValideAge}




